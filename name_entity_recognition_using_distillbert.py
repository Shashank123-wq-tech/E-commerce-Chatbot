"""CONVERTING INTO HUGGING FACE FORMAT"""

def convert_to_hf_format(data):

    hf_data = []

    for text, ann in data:

        entities = ann["entities"]

        encoding = tokenizer(
            text,
            return_offsets_mapping=True,
            truncation=True,
            max_length=128
        )

        labels = ["O"] * len(
            encoding["offset_mapping"]
        )

        for start, end, entity in entities:

            first = True

            for idx, (s, e) in enumerate(
                encoding["offset_mapping"]
            ):

                if s >= start and e <= end and s != e:

                    if first:

                        labels[idx] = (
                            f"B-{entity}"
                        )

                        first = False

                    else:

                        labels[idx] = (
                            f"I-{entity}"
                        )

        label_ids = []

        for label in labels:

            label_ids.append(
                label2id[label]
            )

        hf_data.append({

            "text": text,

            "input_ids":
            encoding["input_ids"],

            "attention_mask":
            encoding["attention_mask"],

            "labels":
            label_ids
        })

    return hf_data

