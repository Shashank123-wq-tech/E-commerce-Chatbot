def compute_metrics(p):

    predictions, labels = p

    predictions = np.argmax(
        predictions,
        axis=2
    )

    true_predictions = []

    true_labels = []

    for pred, label in zip(
        predictions,
        labels
    ):

        pred_labels = []

        true = []

        for p_i, l_i in zip(
            pred,
            label
        ):

            if l_i != -100:

                pred_labels.append(
                    id2label[p_i]
                )

                true.append(
                    id2label[l_i]
                )

        true_predictions.append(
            pred_labels
        )

        true_labels.append(
            true
        )

    results = seqeval.compute(

        predictions=true_predictions,

        references=true_labels
    )

    return {

        "precision":
        results["overall_precision"],

        "recall":
        results["overall_recall"],

        "f1":
        results["overall_f1"],

        "accuracy":
        results["overall_accuracy"]
    }

